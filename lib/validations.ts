import { z } from "zod";

export const mobileNumberSchema = z
  .string()
  .min(1, "شماره موبایل الزامی است")
  .regex(/^09\d{9}$/, "شماره موبایل باید با 09 شروع شود و 11 رقم باشد")
  .length(11, "شماره موبایل باید 11 رقم باشد");

export const otpSchema = z
  .string()
  .length(6, "کد تأیید باید 6 رقم باشد")
  .regex(/^\d{6}$/, "کد تأیید باید فقط شامل اعداد باشد");

export const firstNameSchema = z
  .string()
  .min(2, "نام باید حداقل 2 کاراکتر باشد")
  .max(50, "نام نمی‌تواند بیشتر از 50 کاراکتر باشد")
  .regex(/^[\u0600-\u06FFa-zA-Z\s]+$/, "نام باید فقط شامل حروف فارسی یا انگلیسی باشد");

export const lastNameSchema = z
  .string()
  .min(2, "نام خانوادگی باید حداقل 2 کاراکتر باشد")
  .max(50, "نام خانوادگی نمی‌تواند بیشتر از 50 کاراکتر باشد")
  .regex(/^[\u0600-\u06FFa-zA-Z\s]+$/, "نام خانوادگی باید فقط شامل حروف فارسی یا انگلیسی باشد");

